          <h2><span>How To Write a Blog Comment for SEO </span> | May 12, 2019 </h2>
          <div class="clr"></div>
          <p>Posted by <a href="#">Monique M</a> <span>&nbsp;&bull;&nbsp;</span></p>
          <p>Blog commenting can be used as back links for SEO. It is important to focus on leaving comments that are highly likely to be approved by the blog editor. Focus on leaving comments which provides relevant and value added information. For example, sharing your own relevant blog post or article in your comment is more likely approved by the blog editor if it ties in to their topic.</p>
          <p> Back links from blog comments are fantastic for second (2nd) tier back links as shown below when your own blog post or article is involved:</p>
          <p>You can also use blog comment as a first (1st) tier link, which typically is a back link to a service or product page you want to rank. It is a good idea to leave a comment that is solution oriented. Try to stay away from leaving comments that seem like you are just promoting. It can be construed as spamming.</p>
          <p>You will need to start by searching for reputable blogs where you can leave thoughtful comments with a link back to your own blog post or article that provides a back link to your essential SEO landing page. By the way, this SEO landing page is the web page you want to rank in the first page of Google.</p>
          <p>Here are some pointers on how to write your blog comment so it more likely going to be approved:</p>
          <ul>
          <li>Complement the author – Start with complementing the points made on the blog post. Every blogger and writer will always approve comments that agree with their article. If you can be specific in your points the better. Be genuine, so yes you need to read the blog post that you are leaving a comment on.</li>
          <li>Share a link to value added information – Follow by sharing a link to your own written material that is published on the web. This written material is typically in a form of a blog post or a published article. If you do not have one, write one up and add a link to your SEO landing page then publish it on your blog.</li>
          <p>Commenting on blog posts that are related to your website, blog or social accounts is one of the best ways to secure back links and draw attention to your online presence. If you leave blog comments as suggested above, it not only provides you with back links, it will also increase visibility to your own blog or article and establish credibility by showing your expertise by sharing your own web publishing.</p>
          </ul>

